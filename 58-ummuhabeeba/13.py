import numpy
matrix1=numpy.matrix([[1,2,3],[4,5,6],[7,8,9]])
matrix2=numpy.matrix([[1,2,3],[4,5,6],[7,8,9]])
matrix3=numpy.add(matrix1,matrix2)
print("ADDITION MATRIX")
print(matrix3)
matrix4=numpy.subtract(matrix1,matrix2)
print("subtract matrix")
print(matrix4)
matrix5=numpy.matmul(matrix1,matrix2)
print("MULTIPLY MATRIX")
print(matrix5)
matrix6=numpy.matrix([[2,2,2],[2,2,2],[2,2,2]])
matrix7=2*matrix6
print("2 * MATRIX [[2,2,2],[2,2,2],[2,2,2]]")
print(matrix7)
matrix8=numpy.transpose(matrix1)
print("TRANSPOSE OF MATRIX [[1,2,3],[4,5,6],[7,8,9]]")
print(matrix8)
